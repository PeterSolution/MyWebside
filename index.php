    <h1>Java</h1>
    <h2>Moja droga nauki z Java</h2>
    <h2>Java to język który jest dla mnie najnowsszy i poznałem go rok temu. W tym języku tworzyłem aplikacje zarządzania wątkami i aplikacje klienckie</h2>
    <br>
    <h2>Jaki to jezyk</h2><br>
    <p>Java to obiektowy język programowania, znany z swojej filozofii "write once, run anywhere". Kluczowe cechy Javy to:</p>
    <ul>
