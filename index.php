    <h1>C#</h1>
    <h2>Moja droga z C#</h2><br>
    <h2>C# to mój glólny język programowania gdzie pisałem programy w sieci lan, aplikacje serwerowe typu Rest API oraz aplikacje klienckie
    </h2><br>
    <h2>jaki to jezyk</h2><br>
    <h2>
    C# to nowoczesny, wieloparadygmatowy język programowania, który łączy w sobie cechy programowania obiektowego i funkcjonalnego. Charakteryzuje się silnym typowaniem i składnią zbliżoną do C++ i Javy. C# oferuje zaawansowane funkcje, takie jak:</h2>
    <ul>
        <li>Garbage collection do automatycznego zarządzania pamięcią</li>
        <li>Właściwości, które umożliwiają łatwe implementowanie getterów i setterów</li>
        <li>LINQ (Language Integrated Query) do efektywnego przetwarzania danych</li>
        <li>Async/await do asynchronicznego programowania</li>
        <li>Rozszerzalne typy wartości (structs) i referencyjne (classes)</li>
    </ul>
    C# ewoluuje wraz z każdym nowym wydaniem, wprowadzając innowacyjne funkcje jak pattern matching czy nullable reference types.
    </h2><br>
    
